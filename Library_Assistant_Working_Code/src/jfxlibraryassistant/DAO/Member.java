package jfxlibraryassistant.DAO;

/**
 *
 * @author ChanMyaeOo
 */
public class Member {
    String id,name,mobile,email;

    public Member(String id, String name, String mobile, String email) {
        this.id = id;
        this.name = name;
        this.mobile = mobile;
        this.email = email;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getMobile() {
        return mobile;
    }

    public String getEmail() {
        return email;
    }
    
}
